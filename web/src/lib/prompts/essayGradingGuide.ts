ルール②：抽象表現を具体化する



解答文の中に指示語（「それ」「これ」「その」など）や比喩、その他わかりにくい抽象的な言葉が含まれていると、採点者はその内容を正確に判断できません。これらの表現は、必ず本文中にある具体的な言葉や内容に置き換えて説明する必要があります。



ルール③：具体例を抽象化・簡潔化する



ルール②とは逆に、本文中の具体的なエピソードや例をそのまま抜き出すだけでは、設問の意図に答えたことにはなりません。その具体例が「つまり何を意味しているのか」を短い言葉でまとめ、抽象化・要約する技術が求められます。この簡潔化によって、解答の核心が明確になります。



ルール④：設問の文末表現に合わせる



これは記述問題の基本中の基本です。設問の問いかけの形式と、解答の文末表現は必ず一致させなければなりません。これにより、設問に真正面から答えているという意思表示になります。



