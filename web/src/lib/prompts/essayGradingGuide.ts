1.3. 得点に直結する「解答の4大ルール」



解答の要素を正しく見つけ出すだけでは、満点を取ることはできません。その内容が採点者に明確に、そして誤解なく伝わる「わかりやすい答案」を作成することが不可欠です。ここで紹介する「解答の4大ルール」は、減点を防ぎ、満点を獲得するための答案作成に欠かせない、文章表現の技術です。



ルール①：主語・述語を明確にする



