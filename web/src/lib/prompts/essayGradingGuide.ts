傍線部で使われている言葉と、同じ意味や似た意味を持つ言葉（同義語・類似語）が、本文中の他の箇所で使われていないかを探します。これらの言葉は、傍線部の意味を別の角度から説明していることが多く、理解を深めるための重要なヒントとなります。



ルール⑤：省略された要素を補う



日本語の文章では、文脈から明らかな主語、述語、目的語などが省略されることが頻繁にあります。傍線部を解釈する際は、「誰が」「何を」「どうした」といった省略された要素を文脈から正確に補い、完全な文として理解することが重要です。



