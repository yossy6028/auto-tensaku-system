1.2. 難問を分解する技術「短く分けて考える」



長い問題文や複雑な構造を持つ傍線部を前にすると、多くの受験生はどこから手をつけていいかわからず、思考が停止してしまいます。この困難を乗り越えるための最も強力な武器が、「短く分けて考える」という技術です。このアプローチは、一度に全体を処理しようとする思考の混乱を防ぎ、解答に必要な要素を一つひとつ着実に見つけ出すことを可能にします。一見すると難解な問題も、細かく分解すれば、単純なパーツの集合体に過ぎないことがわかります。



